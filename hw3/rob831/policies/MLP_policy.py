class MLPPolicyAC(MLPPolicy):
    def update(self, observations, actions, adv_n=None):
        # In the actor, we wish to approximate the policy, or, deciding which action to take

        # Convert from numpy to tensors
        obs = torch.from_numpy(observations).to(ptu.device)
        acts = torch.from_numpy(actions).to(ptu.device)
        adv  = torch.from_numpy(adv_n).to(ptu.device)

        # This is the distribution of our actions in a stochastic context
        act_prob_dist = self.forward(obs)

        # Use this distribution and get the log probability. AKA, how likely are these acitons that occured
        act_log_prob = act_prob_dist.log_prob(acts)

        # Use the advantage estimates that tell us how good is this state an action compared to the average value of the state
        # From here, we can weight these advantages for each timestep by the probability of an action occuring.
        # In the long game, high advantages with low probability will be boosted (these actions will have their probability bumped)
        # We take the negative here because we are minimizing the log probability this will turn it into a convex minimization
        loss = -torch.sum(act_log_prob * adv)

        # With the loss value, let's update our parameters to choose better actions in the future
        self.optimizer.zero_grad()
        loss.backward()
        self.optimizer.step()

        return loss.item()
